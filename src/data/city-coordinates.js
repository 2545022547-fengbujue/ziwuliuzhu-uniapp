// 转换为标准格式（按拼音排序）
// 处理城市名称：去除"市"/"地区"后缀，保留"州"/"盟"等行政区划后缀
export const CITIES = cityData
  .map(([province, name, longitude, pinyin, abbr]) => {
    return {
      name: name.replace(/市$/, '').replace(/地区$/, ''),
      province,
      longitude,
      pinyin,
      abbr
    }
  })
  .sort((a, b) => a.pinyin.localeCompare(b.pinyin))

/**
 * 根据名称/拼音/首字母搜索城市
 * 
 * 搜索规则：
 * - 输入1-2个字母：精确匹配拼音首字母缩写（abbr）
 * - 输入3个及以上字母：匹配城市名称、拼音、省份
 * 
 * @param {string} keyword - 搜索关键词
 * @returns {Array} 匹配的城市列表
 * 
 * @example
 * searchCities('bj')     // 返回：[{name: '北京', ...}, {name: '宝鸡', ...}]
 * searchCities('北京')   // 返回：[{name: '北京', ...}]
 * searchCities('广东')   // 返回：广东省所有城市
 */
export function searchCities(keyword) {
  if (!keyword) return CITIES
  // 用户可能输入"贵阳市"，但存储名是"贵阳"，搜索时去除"市"后缀
  const kw = keyword.replace(/市$/, '').toLowerCase()

  // 输入1-2个字母时，只匹配拼音首字母缩写（abbr）
  // 例如：'bj' → 北京、宝鸡
  if (kw.length <= 2 && kw.match(/^[a-z]+$/)) {
    return CITIES.filter(city =>
      city.abbr.toLowerCase() === kw
    )
  }

  // 输入3个及以上字母时，匹配拼音或城市名称
  // 例如：'beijing' → 北京, '广东' → 广东省所有城市
  return CITIES.filter(city =>
    city.name.toLowerCase().includes(kw) ||
    city.pinyin.toLowerCase().includes(kw) ||
    city.province.toLowerCase().includes(kw)
  )
}

/**
 * 根据城市名称获取经度
 * 
 * 用途：真太阳时计算时需要城市经度
 * 
 * @param {string} cityName - 城市名称（不含"市"后缀）
 * @returns {number|null} 经度，未找到返回null
 * 
 * @example
 * getCityLongitude('北京')  // 返回：116.40
 * getCityLongitude('未知')  // 返回：null
 */
export function getCityLongitude(cityName) {
  const city = CITIES.find(c => c.name === cityName)
  return city ? city.longitude : null
}

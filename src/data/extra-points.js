/**
 * 获取经外奇穴完整信息
 * @param {string} code - 穴位代码（如 EX-HN1, EX-UE1）
 * @returns {Object|null} 穴位完整信息
 */
export function getExtraPointByCode(code) {
  return EXTRA_POINTS.find(point => point.code === code) || null
}

/**
 * 获取所有经外奇穴
 * @returns {Array} 所有经外奇穴
 */
export function getAllExtraPoints() {
  return EXTRA_POINTS
}

/**
 * 搜索经外奇穴
 * @param {string} keyword - 搜索关键词
 * @returns {Array} 搜索结果
 */
export function searchExtraPoints(keyword) {
  if (!keyword) return []
  
  return EXTRA_POINTS.filter(point =>
    point.name.includes(keyword) ||
    point.code.toLowerCase().includes(keyword.toLowerCase()) ||
    (point.pinyin && point.pinyin.toLowerCase().includes(keyword.toLowerCase()))
  )
}
